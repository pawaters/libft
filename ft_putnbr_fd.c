#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		ft_putnbr_fd(147483648);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr_fd(-n);
	}
	else
	{
		if (n > 9)
		{
			ft_putnbr_fd(n / 10);
		}
		i = n % 10 + '0';
		write (fd, &i, 1); 
	}
}
