int	strncmp(const char	*s1, const	char	*s2, size_t	n)
{
	int i;

	i = 0;
	while (*s1 != '\0' && *s1 == *s2 && i < n)
	{
		s1++;
		s2++;
		i++;
	}
	if (*s1 - *s2 > 0)
		return (1);
	else if (*s2 - *s1 > 0)
		return (-1);
	else
		return (0);
}
